# üì§ Funci√≥n para exportar Excel con estilo
def exportar_excel(datos):
    df = pd.DataFrame([
        {
            "√çTEM": i + 1,
            "DESCRIPCI√ìN": campo,
            "UNIDAD": "",
            "REQUERIDO": valor
        }
        for i, (campo, valor) in enumerate(datos.items())
    ])

    output = BytesIO()
    with pd.ExcelWriter(output, engine='openpyxl') as writer:
        df.to_excel(writer, index=False, sheet_name="CTG", startrow=3)
        wb = writer.book
        ws = writer.sheets["CTG"]

        # T√≠tulo
        ws.merge_cells("A1:D1")
        ws["A1"] = f"DESCARGADORES DE SOBRETENSI√ìN {nivel_tension} kV"
        ws["A1"].font = Font(bold=True, size=14)
        ws["A1"].alignment = Alignment(horizontal="center")

        # Encabezados con estilo
        header_fill = PatternFill(start_color="003366", end_color="003366", fill_type="solid")
        header_font = Font(color="FFFFFF", bold=True)
        thin_border = Border(
            left=Side(style='thin'), right=Side(style='thin'),
            top=Side(style='thin'), bottom=Side(style='thin')
        )

        for col_num in range(1, 5):
            cell = ws.cell(row=4, column=col_num)
            cell.fill = header_fill
            cell.font = header_font
            cell.alignment = Alignment(horizontal="center")
            cell.border = thin_border

        # Ajuste de columnas
        ws.column_dimensions["A"].width = 10
        ws.column_dimensions["B"].width = 50
        ws.column_dimensions["C"].width = 15
        ws.column_dimensions["D"].width = 20

        # Formato de filas
        for row in ws.iter_rows(min_row=5, max_row=ws.max_row, max_col=4):
            for cell in row:
                cell.border = thin_border
                cell.alignment = Alignment(vertical="center", wrap_text=True)
            row[0].alignment = Alignment(horizontal="center", vertical="center")

    output.seek(0)
    return output

# üì• Bot√≥n para generar y descargar
